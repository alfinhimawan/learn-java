package learn.learn_java;

public class kondisi_if_else {
    public static void main(String[] args) {
        int nilai = 60;

        // Kondisi if...else
        if (nilai > 70) {
            System.out.println("Nilai Anda di atas rata-rata");
        } else {
            System.out.println("Nilai Anda di bawah rata-rata");
        }
    }
}
