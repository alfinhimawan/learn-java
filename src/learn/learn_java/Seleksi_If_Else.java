package learn.learn_java;

public class Seleksi_If_Else {
    public static void main(String[] args) {
        int x;
        x = 10;
        System.out.println("Nilai x = " + x);
        if (x > 5) {
            System.out.println("Jika nilai x lebih dari 5");
            System.out.println("Maka pernyataan pertama yangdieksekusi");
        } else {
            System.out.println("Jika nilai x tidak lebih dari 5");
            System.out.println("Maka pernyataan kedua yang dieksekusi");
        }

    }
}
