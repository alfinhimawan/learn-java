package learn.learn_java;

public class While {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("Sebelum while");
        while (a >= 10) {
            System.out.println("Nilai a : " + a);
            a--;
        }
        System.out.println("Setelah while");
    }
}
